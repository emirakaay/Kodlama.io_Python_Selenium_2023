#HTML NEDİR?

# Web tasarım konusunda araştırma yapan hemen herkesin karşına çıkan temel kavramlardan biri de HTML'dir.
# Web sayfalarını oluşturma aşamasında kullanılan standart bir metin işaret dili olan HTML'nin açılımı “Hyper Text Markup Language” olarak bilinir.
# Genel bilinen yanlış kanının aksine HTML bir programlama dili değildir. 
# Daha açık anlatmak gerekirse, Chrome, Firefox, Yandex gibi tarayıcıların okuyup anlamlandırdığı dil HTML dilidir.


